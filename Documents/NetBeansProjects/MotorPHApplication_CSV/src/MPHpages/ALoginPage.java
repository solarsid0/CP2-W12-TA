    private void signinPBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinPBActionPerformed
         //Sign In Button
        String id = empIDTF.getText();
        char[] passwordArray = passwordPF.getPassword();
        String password = new String(passwordArray);

        if (id.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Some Fields Are Empty", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean validCredential = false;
            String firstName = "";
            String lastName = "";
            String userRole = "";

            if (credentialsMap.containsKey(id)) {
                String[] userCredentials = credentialsMap.get(id);
                String storedPassword = userCredentials[0];
                userRole = userCredentials[1];
                lastName = userCredentials[2];
                firstName = userCredentials[3];

                if (password.equals(storedPassword)) {
                    validCredential = true;
                }
            }

            if (validCredential) {
                JOptionPane.showMessageDialog(rootPane, "Login Successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
                
                //Redirects to the Employee Portal
                MPHpages.EmployeePortal.EmployeePortal employeePage = new EmployeePortal(firstName + " " + lastName, id, userRole);
                employeePage.setLocationRelativeTo(null);
                employeePage.setVisible(true);

                // Close LoginPage
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "ID or Password Not Matched", "Login Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_signinPBActionPerformed

