    private Map<String, String[]> readCredentialsFromCSV(String filePath) {
        Map<String, String[]> credentialsMap = new HashMap<>();
        String line = "";
        String csvSplitBy = ",";
        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            br.readLine(); // Skip header
            while ((line = br.readLine()) != null) {
                String[] values = line.split(csvSplitBy);
                String id = values[0];
                String password = values[1];
                String userRole = values[2];
                String lastName = values[3];
                String firstName = values[4];
                credentialsMap.put(id, new String[]{password, userRole, lastName, firstName});
            }
        } catch (IOException e) {
        }
        return credentialsMap;
    }
    
    
    
